package com.ul.taxi.car.model;

public class Car {
	private int carid;
	private int carno;
	private String cartype;
	private String divername;
	private int diverphone;
	private int price;	
	private int mark;
	private String state;
	public int getCarid() {
		return carid;
	}
	public void setCarid(int carid) {
		this.carid = carid;
	}
	public int getCarno() {
		return carno;
	}
	public void setCarno(int carno) {
		this.carno = carno;
	}
	public String getCartype() {
		return cartype;
	}
	public void setCartype(String cartype) {
		this.cartype = cartype;
	}
	public String getDivername() {
		return divername;
	}
	public void setDivername(String divername) {
		this.divername = divername;
	}
	public int getDiverphone() {
		return diverphone;
	}
	public void setDiverphone(int diverphone) {
		this.diverphone = diverphone;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getMark() {
		return mark;
	}
	public void setMark(int mark) {
		this.mark = mark;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	


	

}
